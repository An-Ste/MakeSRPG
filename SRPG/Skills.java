package SRPG;

public class Skills {
	
	public int physics_power_up(int physics_power) {
		physics_power += 2;
		return physics_power;
	}
	
	public int physics_defense_up(int physics_defense) {
		physics_defense += 2;
		return physics_defense;
	}
	
	public int magic_power_up(int magic_power) {
		magic_power += 2;
		return magic_power;
	}
	
	public int magic_defense_up(int magic_defense) {
		magic_defense += 2;
		return magic_defense;
	}
}
